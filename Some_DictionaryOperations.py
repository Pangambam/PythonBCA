# Dictionary creation
student = {
    "name": "Anjali",
    "age": 20,
    "marks": 90
}

# Accessing values
print("Name:", student["name"])

# Adding a new key-value pair
student["grade"] = "A"

# Updating a value
student["marks"] = 95

# Removing a key
del student["age"]

print("Updated student dictionary:", student)
