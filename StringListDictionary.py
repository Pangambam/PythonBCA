# String functions
s = "Hello World"
print("Length of string:", len(s))
print("Is string alphanumeric?", s.isalnum())

# List functions
l = [1, 2, 3, 4]
print("Max value in list:", max(l))
print("Sum of list elements:", sum(l))

# Dictionary functions
d = {"a": 1, "b": 2, "c": 3}
print("Keys:", d.keys())
print("Values:", d.values())
