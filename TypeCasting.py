# Integer to float
num = 10
print("Integer:", num)
print("Float:", float(num))

# Float to integer
decimal = 9.87
print("Float:", decimal)
print("Integer:", int(decimal))

# String to integer
str_num = "100"
print("String:", str_num)
print("Integer:", int(str_num))

# Integer to string
age = 25
print("Integer:", age)
print("String:", str(age))
